function assignSynonyms(&$userData) {
    global $synonyms;
    $keys = array_keys($userData['data']);
    
    foreach ($keys as $key) {
        $userData['data'][$key]['properties']['synonyms'] = (isset($synonyms[$key])) ? $synonyms[$key] : (array());
    }
}


function createReverseLookupCategoryHash() {
    global $synonyms;
    $reverseLookupCategoryHash = array();
    
    foreach ($synonyms as $word=>$synonymsList) {
        $reverseLookupCategoryHash[$word] = array($word);
        foreach ($synonymsList as $synonym) {
            if (!isset($reverseLookupCategoryHash[$synonym]))
                $reverseLookupCategoryHash[$synonym] = array();
            $reverseLookupCategoryHash[$synonym][] = $word;
        }
    }

    return $reverseLookupCategoryHash;
    
    
